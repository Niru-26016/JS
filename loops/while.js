//1. While loop that calculate the sum of all numbers from 1 to 5
let sum = 0;
i = 1;
while (i <= 5) {
  sum += i;
  i++;
}
console.log(sum);

//2. Write a while loop that counts down from 5 to 1 and stores the variable in an array

let countdown = [];
let j = 5;
while (j > 0) {
  console.log(j);
  countdown.push(j);
  j--;
}
console.log(countdown);

